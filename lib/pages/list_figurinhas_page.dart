class _ListFigurinhasPageState extends State<ListFigurinhasPage> {
  //const ListFigurinhasPage({Key? key}) : super(key: key);
  late List<bool> gridValue;

  @override
  void initState() {
    super.initState();
    gridValue = List<bool>.filled(widget.figurinhasLabel.length, true);
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: const Text('Lista de Figurinhas'),
      ),
      body: GridView.builder(
          itemCount: widget.figurinhasLabel.length,
          gridDelegate: const SliverGridDelegateWithFixedCrossAxisCount(
            crossAxisCount: 5,
            crossAxisSpacing: 4.0,
            mainAxisSpacing: 4.0,
          ),
          itemBuilder: (BuildContext context, int index) {
            String figurinhaLabel = widget.figurinhasLabel[index];
            return ItemFigurinha(
              figurinhaLabel: figurinhaLabel,
              corFundo: Colors.red,
              corTexto: Colors.white,
              isActive: gridValue[index],
              onTap: () {
                setState(() {
                  gridValue[index] = !gridValue[index];
                });
              },
            );
          }),
    );
  }
}
